using Microsoft.AspNetCore.Localization;
using Microsoft.EntityFrameworkCore;
using Microsoft.Extensions.Options;
using System.Globalization;
using TaxiRidesLocalization.Data;

var builder = WebApplication.CreateBuilder(args);

// ????????????: ?????? services ? ????? ???? ?? ?????????
builder.Services.AddLocalization(options => options.ResourcesPath = "Resources");

// ????????????? ?? ????????? ??????
var supportedCultures = new[] { new CultureInfo("en-US"), new CultureInfo("mk-MK") };

builder.Services.Configure<RequestLocalizationOptions>(options =>
{
    options.DefaultRequestCulture = new RequestCulture("mk-MK"); // default ?? ??????????
    options.SupportedCultures = supportedCultures;
    options.SupportedUICultures = supportedCultures;

    // ??????????? ?? cookie ??????? (????? ?? ????? ??????? ?? ?????)
    options.RequestCultureProviders.Insert(0, new CookieRequestCultureProvider());
});

// ????? HttpContextAccessor
builder.Services.AddHttpContextAccessor();

// ???????????? ?? DB context-?? (SQL Server)
builder.Services.AddDbContext<ApplicationDbContext>(options =>
    options.UseSqlServer(builder.Configuration.GetConnectionString("TaxiServiceDBCon")));

// ????? Razor Pages ?? ???????? ?? View ????????????
builder.Services.AddRazorPages()
    .AddViewLocalization(); // ????? ?? @inject IViewLocalizer

// ????? Controllers ?? CultureController
builder.Services.AddControllers();

var app = builder.Build();

// Middleware ?? ???????????? (???? ???? UseRouting)
var locOptions = app.Services.GetRequiredService<IOptions<RequestLocalizationOptions>>();

// ?????? UseRequestLocalization ?? ??????????????? ?????
app.UseRequestLocalization(locOptions.Value);

// ???????? ???????
app.UseStaticFiles();

// Routing
app.UseRouting();

// Mapiraj Controllers
app.MapControllers();

// Mapiraj Razor Pages
app.MapRazorPages();

app.Run();
